# Upper Case
a = "Hello, world!"
print(a.upper()) # HELLO, WORLD!

# Lower Case
a = "Hello, world!"
print(a.lower())# hello, world!

# Remove whitespaces
a = " Hello, world! "
print(a.strip()) # "Hello, world!"

# Replace String
a = "Hello, world!"
print(a.replace("H","J")) # Jello, world!

# Split String
a = "Hello, world!"
print(a.split(",")) # ['Hello', 'world!']